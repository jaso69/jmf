    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
         $notas = Mensaje::where('id_users', auth()->user()->id)->paginate(10);
        return view('mensajes.mensajes', compact('notas'));
    }

